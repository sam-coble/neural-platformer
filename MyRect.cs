using System;
namespace BrainGame
{
    public class MyRect
    {
        public double X;
        public double Y;
        public double Width;
        public double Height;
        public MyRect(double x, double y, double width, double height)
        {
            this.X = x;
            this.Y = y;
            this.Width = width;
            this.Height = height;
        }
    }
}
